const ALL_IMAGE_BACKGROUND_OPTIONS = [...IMAGE_BACKGROUND_OPTIONS, ...MORE_IMAGE_BACKGROUND_OPTIONS]

export const COLOR_BACKGROUND_OPTIONS = [
  '#0079bf',
  '#d29034',
  '#519839',
  '#b04632',
  '#89609e'
]

export const DEFAULT_BOARD_BACKGROUND = {
  kind: 'image',
  value: IMAGE_BACKGROUND_OPTIONS[0].full
}

function hasImage(value) {
  return ALL_IMAGE_BACKGROUND_OPTIONS.some((option) => option.full === value)
}

function hasColor(value) {
  return COLOR_BACKGROUND_OPTIONS.includes(value)
}

export function normalizeBoardBackground(background) {
  const fallback = { ...DEFAULT_BOARD_BACKGROUND }

  if (!background || typeof background !== 'object') {
    return fallback
  }

  const kind = background.kind === 'color' ? 'color' : 'image'
  const value = typeof background.value === 'string' ? background.value : ''

  if (!value) {
    return fallback
  }

  if (kind === 'image' && !hasImage(value)) {
    return fallback
  }

  if (kind === 'color' && !hasColor(value)) {
    return fallback
  }

  return {
    kind,
    value
  }
}

export function getBoardBackgroundStyle(background, options = {}) {
  const normalized = normalizeBoardBackground(background)
  const withOverlay = Boolean(options.withOverlay)

  if (normalized.kind === 'image') {
    if (withOverlay) {
      return {
        backgroundImage: `linear-gradient(180deg, rgba(9, 30, 66, 0.18), rgba(9, 30, 66, 0.38)), url("${normalized.value}")`,
        backgroundSize: 'cover',
        backgroundPosition: 'center'
      }
    }

    return {
      backgroundImage: `url("${normalized.value}")`,
      backgroundSize: 'cover',
      backgroundPosition: 'center'
    }
  }

  return {
    background: normalized.value
  }
}
